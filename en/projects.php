			<h2><abbr title="Teams of Robots for Service and Security Missions">TESSEO</abbr> </h2>
			<p>
				The project proposes to investigate techniques for a multi-robot team to act in coordination in realistic scenarios.
				For the deployment, it is necessary to deal with algorithms and methods related to task planning and allocation, coordinated navigation planning, environment perception from multiple views provided by every member of the team, while the communication connectivity among all the elements of the system is maintained - robots, infrastructure, supervisor team, etc.
			</p>
			<p>	
				Although some of the techniques involved are usually proposed in the literature and in many projects somehow independently, the research in this project will also be oriented to develop techniques integrating the different subjects involved. 
				Only in this way it will be possible to develop realistic applications using systems with autonomous and supervised behaviours. 
			</p>
			<p>
				Within the wide spectrum of scientific and technological challenges that appears in this kind of systems, several research objectives, grouped in three interdependent blocks, are going to be tackled in this project.  <a href="http://robots.unizar.es/html/ficha_proyecto.php?id=26">More info</a>.
			</p>
		</li>

		<li>
			<h2><abbr title="Ubiquitous networking Robotics in Urban Settings">URUS</abbr></h2>
			<p>
				European cities are becoming difficult places to live due to noise, pollution and security. 
				Moreover, the average age of people living European cities is growing and in a short period of time there will be an important community of elderly people.
			</p>
			<p>
				City Halls are becoming conscious of this problem and are studying solutions, for example by reducing the free car circulation areas. 
				Free car areas imply a revolution in the planning of urban settings, for example, by imposing new means for transportation of goods, security issues, etc. 
			</p>
			<p>
				In this project we want to analyse and test the idea of incorporating a network of robots (robots, intelligent sensors, devices and communications) in order to improve life quality in such urban areas. <a href="http://robots.unizar.es/html/ficha_proyecto.php?id=20&idmember=">More info</a>.
			</p>
		</li>
					
		<li>
			<h2><abbr title="Networked Robots">NERO</abbr></h2>
			<p>
				The complex nature of mobile robot tasks leads to the necessity of systems with several coordinated robots (agents) working in cooperation.
			</p>
			<p>
				Some international directives refer to robotic elements connected to the communication nets or wireless nets including the robots themselves and the sensors distributed in the working place (static agents) exchanging and sharing information. 
				This concept is extended to robot interactions between humans, the sensors and the environment.
			</p>
			<p>
				We propose this project which is very related with previous MEC projects obtained by this research team, to continue working on subjects related to multi-robot cooperation techniques, computer vision, robot vision for motion and communications. <a href="http://robots.unizar.es/html/ficha_proyecto.php?id=19">More info</a>.
			</p>
		</li>
